const loadHyperUiBlocks = (newEditor: { BlockManager: any }): void => {
  const blockManager = newEditor.BlockManager

  sources.forEach((s) => {
    blockManager.add(s.id, {
      label: s.label,
      attributes: { class: s.class },
      content: s.content,
      category: { label: s.category, open: s.category === 'Banner' },
    })
  })
}
